package ar.edu.utn.frc.tup.lc.iv.dtos.common.enums;

/**
 * Enum representing the context in which a proof is provided.
 */
public enum ProofContext {
    /**
     * Proof provided to justify a claim.
     */
    CLAIM_JUSTIFICATION,

    /**
     * Proof provided as a defense against an infraction.
     */
    DEFENSE
}
